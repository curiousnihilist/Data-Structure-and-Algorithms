package com.problems;

public class NameValue {
	private int element;
	private int index;
	
	public NameValue(int element, int index) {
		this.element = element;
		this.index = index;
	}

	public int getElement() {
		return element;
	}

	public void setElement(int element) {
		this.element = element;
	}

	public int getIndex() {
		return index;
	}

	public void setIndex(int index) {
		this.index = index;
	}
	
	

}
